package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {
        byte number0 = 5;
        short number = 256;
        int number1 = 100;
        long number2 = 20000;
        float number3 = 251.5f;
        double number4 = 5469825.547;

        System.out.println(100 + 200);
        System.out.println(number1+number0);
        System.out.println(number4/number2);
        System.out.println(number0-number1);
        System.out.println(number4*number3);
        System.out.println(number%number0);

    }
}
