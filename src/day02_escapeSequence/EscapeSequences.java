package day02_escapeSequence;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello World\nHow are you today?");
        System.out.println("--------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch1");
        System.out.println("-------------------------------------");

        System.out.println("\\Hello students\\");
        System.out.println("------------------------------------");

        System.out.println("My favourite film \"Titanic\"");
        System.out.println("------------------------------------");


    }



}
