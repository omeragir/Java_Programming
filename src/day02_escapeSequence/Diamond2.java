package day02_escapeSequence;

public class Diamond2 {
    public static void main(String[] args) {

        System.out.println("     ^");
        System.out.println("   /    \\");
        System.out.println("  /  *  \\");
        System.out.println(" / *   * \\");
        System.out.println("/ *  *  * \\");
        System.out.println(" _________");
        System.out.println("\\ *  *  * /");
        System.out.println(" \\ *   * /");
        System.out.println("  \\  *  /");
        System.out.println("   \\   /");
        System.out.println("     V");
    }


}
