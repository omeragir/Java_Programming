package day05_operators;

public class UnaryOperators {
    public static void main(String[] args) {
        //pre increment

        int a=25;
        System.out.println(++a);
        System.out.println("---------------------");
        int b=50;
        System.out.println(--b);

        System.out.println("---------------------");
        int x=100;
        System.out.println(x++);

        System.out.println(x);
        System.out.println("---------------------");
        int y=90;
        System.out.println(y--);
        System.out.println(y);

    }
}
